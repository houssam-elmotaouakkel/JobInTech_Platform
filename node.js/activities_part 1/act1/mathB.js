const PI = 3.14;                     // Had constante katmattel l valeur dyal PI (pi) li hiya 3.14

function CalculerAire(rayon) {     // Had l-fonction kat7seb l'aire dyal cercle (mintaqa) 3la 7sab rayon
    return PI * rayon * rayon;    // Katrja3 (return) natija dyal 3.14 * rayon * rayon
}

console.log(CalculerAire(2));  // Hna kan3ayto l-fonction b rayon = 2 bach nchofo ch7al hiya l'aire